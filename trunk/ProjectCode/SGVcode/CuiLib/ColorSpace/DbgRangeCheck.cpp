void  DbgRangeCheck::Check01(const double _f)
 {
	 const double EPS = 1E-1;
	 CheckRange(_f,0, 1, EPS);
 }
/*----------------------------------------------------------------*/
/**
*
*/
/*----------------------------------------------------------------*/
inline void  DbgRangeCheck::CheckRange(const double& _f,const double& _min, const double& _max,const double& _eps)
{
	assert(_f >= _min - _eps && _f <= _max + _eps);
}
